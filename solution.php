        <p class="tag">According to the data collected by EO Dashboard, the COVID – 19 pandemics have had an impact on the density of autos in international airports in the United States. The number of automobiles in airports has decreased significantly after the COVID-19 pandemics, as shown in <strong><em>(figure 1)</em></strong>; nevertheless, this significant reduction began in March 2020 and has continued unabated since then. Since the lockdown due to the COVID-19, the traffic has vanished as the density of cars has decreased. This reduction in car density in the airport's by-parking areas substantially impacts two aspects: economics and air quality.</p>
